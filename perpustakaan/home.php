                    <br><u class="main-text">Gambaran Umum</u></br>
                      <p class="text-muted" align="justify"> Perpustakaan Sekolah Menengah Kejuruan Tribakti Pangalengan merupakan salah satu pelaksana kebijakan Sekolah yang bersifat spesifik di bidang Perpustakaan. Keberadaan perpustakaan mendorong terwujudnya cita-cita yang diamanatkan dalam Undang-undang Dasar tahun 1945 yaitu mencerdaskan kehidupan bangsa. Sehubungan dengan itu, maka tujuan perpustakaan adalah memberikan layanan kepada pemustaka, meningkatkan kegemaran membaca, serta memperluas wawasan dan pengetahuan untuk mencerdaskan kehidupan bangsa. Dalam rangka mencerdaskan kehidupan bangsa tersebut perlu ditumbuhkembangkan budaya gemar membaca melalui perpustakaan, perpustakaan juga sebagai wahana belajar sepanjang hayat (long life educations).Perpustakaan Sekolah Menengah Kejuruan Tribakti Pangalengan sebagai penanggungjawab dalam mewujudkan pembinaan minat baca di lingkungan sekolah dan penjamin terselamatkannya dan terlestarinya serta didayagunakannya budaya gemar membaca dan menciptakan budaya literasi.</p>
