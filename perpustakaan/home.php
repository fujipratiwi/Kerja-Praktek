                    <u><p class="main-text">Sejarah Singkat</p></u>
                    <p class="text-muted" align="justify">Perpustakaan Sekolah adalah sebuah tempat penunjang kegiatan belajar mengajar dalam mencari sumber informasi dan sebagai sarana rekreasi baca siswa-siswi di lingkungan sekolah. Mengingat bahwa sarana dan prasarana merupakan salah satu unsur Penunjang Pendidikan pada Satuan Pendirian Perpustakaan dan Perpustakaan merupakan kebutuhan anak didik dan meningkatkan kecerdasan dan keterampilan. 
                    Sejarah Perpustakaan Sekolah Menengah Kejuruan Tribakti Pangalengan sebenarnya di awali dengan berdirinya SMK Tribakti yang dulunya adalah SMA Tribakti, yaitu pada tahun 1985. Dalam Perjalanannya di era yang semakin maju perpustakaan Sekolah Menengah Kejuruan Tribakti Pangalengan selalu mengalami dinamika perubahan dalam pembenahan perpustakaan baik dari segi fisik perpustakaan maupun program-program kerja untuk mengembangkan perpustakaan di Sekolah Menengah Kejuruan Tribakti Pangalengan agar lebih Maju, Berkarakter dan dapat menjadi sarana Penunjang utama dan bermanfaat bagi siswa-siswi maupun warga sekolah yang berada di Lingkungan Sekolah Menengah Kejuruan Tribakti Pangalengan.</p>
